  constructor(private route: ActivatedRoute, private router: Router) {}

  ngOnInit() {
    // Get game ID from the URL
    
    this.gameId = Number(this.route.snapshot.paramMap.get('id'));

    // Load the correct card questions and image for the game based on the game ID
    this.cards = this.gameCardQuestions[this.gameId] || []; // Fallback to empty array if no match
    this.cardImage = this.gameCardImages[this.gameId] || ''; // Fallback to empty string if no match

    // Get the game's title by matching the gameId
    const game = this.games.find(game => game.id === this.gameId);
    if (game) {
      this.gameTitle = game.name;
    }

    // Check for saved card progress in localStorage
    const savedCardIndex = localStorage.getItem(`game-${this.gameId}-progress`);
    if (savedCardIndex) {
      this.currentCardIndex = Number(savedCardIndex);
    }
    this.adjustFontSize();
  }

  // Navigate to the next card
  nextCard() {
    if (this.currentCardIndex < this.cards.length - 1) {
      this.currentCardIndex++;
      this.adjustFontSize();
      this.saveProgress();
    }
  }

  // Navigate to the previous card
  previousCard() {
    if (this.currentCardIndex > 0) {
      this.currentCardIndex--;
      this.adjustFontSize();
      this.saveProgress();
    }
  }

  // Save current progress in localStorage
  saveProgress() {
    localStorage.setItem(`game-${this.gameId}-progress`, String(this.currentCardIndex));
  }

  adjustFontSize() {
    const maxChars = 40; // Maximum characters before reducing font size
    const baseFontSize = 14; // Default font size
    const minFontSize = 11; // Minimum font size for readability

    // Adjust the font size dynamically based on the current card's text length
    const currentTextLength = this.cards[this.currentCardIndex]?.length || 0;
    if (currentTextLength > maxChars) {
      const reductionFactor = Math.floor((currentTextLength - maxChars) / 10);
      this.fontSize = Math.max(baseFontSize - reductionFactor, minFontSize);
    } else {
      this.fontSize = baseFontSize;
    }
  }
